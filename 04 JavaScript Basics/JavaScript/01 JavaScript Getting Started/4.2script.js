///// 04 Types & Arrays: Section 2 - Type Basics: Strings & Numbers \\\\\

let value = "Model";
console.log( typeof(value) ); // results of log = String

let value = 99;
console.log( typeof(value) ); // results of log = Number

//-------------------------

let value = 4.1 + 4.3;
console.log(value);     // results of log = 8.39999999999

let value = 10 / 0;
console.log(value);     // results of log = Infinity

let value = -10 / 0;
console.log(value);     // results of log = -Infinity

let value = 0 / 0;
console.log(value);     // results of log = NaN (Not a Number)

